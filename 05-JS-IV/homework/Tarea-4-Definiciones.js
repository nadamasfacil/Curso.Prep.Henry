// Un Objeto es una colección de datos y/o funciones relacionados
// Son como un Arreglo pero con características más específicas llamadas
// propiedades en lugar de subíndices.
//
// Propiedades
// Se les llama Propiedades a los nombres que se asignan a una característica
// en especial al tipo de dato o función en el Objeto.
//
// Métodos
// Los Metodos son los datos tipo Funcion() en un Objeto.
// Estas funciones siguen un conjunto de instrucciones para obtener un resuktado.
//
// Bucle for...in
// El bucle for...in es un bucle similar al for.. normal que es realizar un 
// número determinado de iteracciones nada mas que cada iterección no es contabilizada
// por un númerp entero sino por las propiedades que componen un objeto
//
